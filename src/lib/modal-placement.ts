/**
 * Supported modal placements within the viewport.
 */
export enum HubModalPlacement {
	/**
	 * Places the modal in the center of the viewport.
	 */
	Center = 'center',

	/**
	 * Anchors the modal to the start edge of the viewport.
	 */
	Start = 'start',

	/**
	 * Anchors the modal to the end edge of the viewport.
	 */
	End = 'end',

	/**
	 * Anchors the modal to the top edge of the viewport.
	 */
	Top = 'top',

	/**
	 * Anchors the modal to the bottom edge of the viewport.
	 */
	Bottom = 'bottom'
}
